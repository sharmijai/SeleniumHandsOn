package week1.assignment1;

public class CalculatorMethods {
	
//Method Syntax:
	//AccessModifiers returntype methodName(args[])
	
	public int addThreeNumbers(int a,int b,int c) {
		int sum=a+b+c;
		return sum;
	}
	public int subThreeNumbers(int a,int b) {
		int sub=a-b;
		return sub;
	}
	
	public double prodThreeNumbers(double num1,double num2) {
		double prod=num1*num2;
		return prod;
	}
	public float divideThreeNumbers(float num1,float num2) {
		float divide=num1/num2;
		return divide;
	}



}
